def get_random_questions(question_type=None, num=5):
    """随机获取指定类型题目"""
    conn = sqlite3.connect('exams.db')
    cursor = conn.cursor()

    query = 'SELECT * FROM exam_questions'
    if question_type:
        query += f' WHERE question_type = "{question_type}"'
    query += f' ORDER BY RANDOM() LIMIT {num}'

    cursor.execute(query)
    questions = cursor.fetchall()

    result = []
    for q in questions:
        # 解析选项（已正确处理 None 情况）
        options = json.loads(q[3]) if q[3] else None

        # 修正：可靠解析正确答案（处理列表和普通字符串）
        try:
            correct_answer = json.loads(q[4])
            # 仅当解析结果为列表时使用，否则保留原字符串
            if not isinstance(correct_answer, list):
                correct_answer = q[4]
        except (json.JSONDecodeError, TypeError):
            correct_answer = q[4]

        result.append({
            '题型': q[1],
            '题目内容': q[2],
            '选项': options,
            '正确答案': correct_answer,
            '解析': q[5]
        })

    conn.close()
    return result


